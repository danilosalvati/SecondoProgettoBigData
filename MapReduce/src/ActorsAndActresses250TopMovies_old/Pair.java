package ActorsAndActresses250TopMovies_old;

/**
 * Questa classe modella una coppia di valori (String, Integer) rappresentanti
 * la coppia attore e rispettivi film
 *
 */
public class Pair {
	public String actor;
	public Integer movies;

	public Pair(String actor, Integer movies) {
		this.actor = actor;
		this.movies = movies;
	}
};
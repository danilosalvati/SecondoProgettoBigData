package Keywords250TopMovies;

/**
 * Questa classe modella una coppia di valori (String, Integer) rappresentanti
 * la coppia keyword e numero di film
 *
 */
public class Pair {
	public String keyword;
	public Integer movies;

	public Pair(String keyword, Integer movies) {
		this.keyword = keyword;
		this.movies = movies;
	}
};